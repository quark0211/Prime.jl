function model_gf2(ps,z,sel)
    z1,z2 = z
    if sel == 1
        Žü,Žä = ps
        return exp(Žü*(z2-1))*exp(Žü*Žä*(z1-1))
    elseif sel == 2
        Žü,b,Žä = ps
        p = 1/(b+1)
        return (p/(1-(1-p)*z2))^Žü*exp(Žü*Žä*b*(z1-1)/(1-b*(z1-1)))
    elseif sel == 4
        Žü,Žāon,Žāoff,Žä = ps
        Žü = min(600,Žü)
        Žāon = min(40,Žāon)
        Žāoff = min(40,Žāoff)
        Žä = min(20,Žä)
        z1,z2 = z
        dm=1.
        u1=z1-1
        u2=z2-1
        x1=Žü*u1/dm
        x2=Žü*u2/dm
        ╬▒=Žāon/(Žāoff+Žāon)
        r=1+(Žāoff+Žāon)/dm-x1
        ╬Ė=sqrt(Complex(((Žāoff+Žāon)/dm-x1)^2+4*Žāon*x1/dm))
        v1=dm*(r+╬Ė-1)/2
        v2=dm*(r-╬Ė-1)/2
        G=(v1*exp(-v2*Žä)-v2*exp(-v1*Žä))/(dm*╬Ė)*pFq((Žāon/dm, ), ((Žāon+Žāoff)/dm, ), x2)+Žü*u1*(exp(-v2*Žä)-exp(-v1*Žä))/(dm*╬Ė)*Žāon/(Žāoff+Žāon)*pFq((1+Žāon/dm, ), (1+(Žāoff+Žāon)/dm, ), x2)

        return real(G)

    elseif sel == 3
        Žü,Žāu,Žāb,╬╗,Žä = ps
        u1=z1-1
        u2=z2-1
        x1=Žü*u1
        x2=Žü*u2
        ╬▒=1/(╬╗*Žāb+╬╗*Žāu+Žāb*Žāu)
        m=╬╗+Žāb+Žāu-x1
        n=╬╗*Žāu+╬╗*Žāb+Žāb*Žāu-(╬╗+Žāu)*x1
        p=-╬╗*Žāu*x1
        k=(-2*m^3+9*m*n-27*p+3*sqrt(3)*sqrt(Complex((-m^2*n^2+4*n^3+4*m^3*p-18*m*n*p+27*p^2))))^(1/3)
        r1=-m/3-2^(1/3)*(-m^2+3*n)/(3*k)+k/(3*2^(1/3))
        a=-m/3+2^(1/3)*(-m^2+3*n)/(6*k)-k/(6*2^(1/3))
        b=2^(1/3)*(-m^2+3*n)*sqrt(3)/(6*k)+sqrt(3)*k/(6*2^(1/3))
        ╬Æ=(a-r1)^2+b^2
        ╬Ė=sqrt(Complex((╬╗-Žāb)^2-2*Žāu*(╬╗+Žāb)+Žāu^2))
        H0=pFq((╬╗,Žāu), ((╬╗+Žāb+Žāu)/2-╬Ė/2,(╬╗+Žāb+Žāu)/2+╬Ė/2), x2)
        H1=pFq((╬╗,Žāu), (1+(╬╗+Žāb+Žāu)/2-╬Ė/2,1+(╬╗+Žāb+Žāu)/2+╬Ė/2), x2)
        H2=pFq((1+╬╗,Žāu), (1+(╬╗+Žāb+Žāu)/2-╬Ė/2,1+(╬╗+Žāb+Žāu)/2+╬Ė/2), x2)
        H3=pFq((1+╬╗,1+Žāu), (1+(╬╗+Žāb+Žāu)/2-╬Ė/2,1+(╬╗+Žāb+Žāu)/2+╬Ė/2), x2)
        Gf=H0+╬▒*(-Žāb*Žāu+exp(r1*Žä)*x1*╬╗*Žāb*Žāu*(r1+Žāu)/(r1*╬Æ)-exp(a*Žä)*╬╗*x1*Žāu*Žāb*((a^2+b^2)+(2*a-r1)*Žāu)*cos(b*Žä)/(╬Æ*(a^2+b^2))+exp(a*Žä)*╬╗*x1*Žāu*Žāb*((a^2+b^2)*(a-r1)+(a^2-b^2-a*r1)*Žāu)*sin(b*Žä)/(b*╬Æ*(a^2+b^2)))*H1+╬▒*(-╬╗*Žāb+exp(r1*Žä)*x1*╬╗^2*Žāb*Žāu/(r1*╬Æ)-exp(a*Žä)*╬╗^2*x1*Žāb*Žāu*(2*a-r1)*cos(b*Žä)/(╬Æ*(a^2+b^2))+exp(a*Žä)*╬╗^2*x1*Žāb*Žāu*(a^2-a*r1-b^2)*sin(b*Žä)/(b*╬Æ*(a^2+b^2)))*H2+╬▒*(-╬╗*Žāu+exp(r1*Žä)*x1*╬╗*Žāu*(r1+╬╗)*(r1+Žāu)/(r1*╬Æ)-exp(a*Žä)*╬╗*x1*Žāu*((a^2+b^2)*(r1+╬╗+Žāu)+(2*a-r1)*╬╗*Žāu)*cos(b*Žä)/(╬Æ*(a^2+b^2))+exp(a*Žä)*╬╗*x1*Žāu*((a^2+b^2)*(b^2+(a-r1)*(a+╬╗))+((a^2+b^2)*(a-r1)+(a^2-b^2-a*r1)*╬╗)*Žāu)*sin(b*Žä)/(b*╬Æ*(a^2+b^2)))*H3
        return real(Gf)
    end
end